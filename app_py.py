st.write("Enter your **Years of Experience** below to estimate your salary.")

# --- Input ---
user_text = st.text_input("üìÖ Years of Experience", placeholder="e.g., 5")

# --- Prediction Button ---
if st.button("üîç Predict Salary"):
    if not user_text.strip():
        st.error("‚ö† Please enter a value")
    else:
        try:
            years_exp = float(user_text)
            input_data = np.array([[years_exp]])
            result = model.predict(input_data)
            salary = float(result[0])

            # Styled prediction box
            st.markdown(f"<div class='prediction-box'>üí∞ Predicted Salary: ${salary:,.2f}</div>", unsafe_allow_html=True)

        except ValueError:
            st.error("‚ùå Please enter a valid numeric value")

